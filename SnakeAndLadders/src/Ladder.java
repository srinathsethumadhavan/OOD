
public class Ladder {
private int startInt;
private int endInt;


public Ladder(int startInt, int endInt) {
	super();
	this.startInt = startInt;
	this.endInt = endInt;
}
public int getStartInt() {
	return startInt;
}
public void setStartInt(int startInt) {
	this.startInt = startInt;
}
public int getEndInt() {
	return endInt;
}
public void setEndInt(int endInt) {
	this.endInt = endInt;
}

}
